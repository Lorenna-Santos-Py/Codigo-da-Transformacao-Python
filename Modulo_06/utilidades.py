def soma(a, b):
    """Retorna a soma de dois números."""
    return a + b

def subtracao(a, b):
    """Retorna a subtração de dois números."""
    return a - b

def potencia(base, expoente):
    """Retorna a base elevada ao expoente."""
    return base ** expoente
